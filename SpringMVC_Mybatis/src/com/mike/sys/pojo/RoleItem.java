package com.mike.sys.pojo;

public class RoleItem {
	private Integer Mid;
	private Integer FroleId;
	private Integer FitemId;
	private String FroleName;
	private String FitemName;
	private Integer FparentId;
	private Integer FunctionType;
	public Integer getMid() {
		return Mid;
	}
	public void setMid(Integer mid) {
		Mid = mid;
	}
	public Integer getFroleId() {
		return FroleId;
	}
	public void setFroleId(Integer froleId) {
		FroleId = froleId;
	}
	public Integer getFitemId() {
		return FitemId;
	}
	public void setFitemId(Integer fitemId) {
		FitemId = fitemId;
	}
	public String getFroleName() {
		return FroleName;
	}
	public void setFroleName(String froleName) {
		FroleName = froleName;
	}
	public String getFitemName() {
		return FitemName;
	}
	public void setFitemName(String fitemName) {
		FitemName = fitemName;
	}
	public Integer getFparentId() {
		return FparentId;
	}
	public void setFparentId(Integer fparentId) {
		FparentId = fparentId;
	}
	public Integer getFunctionType() {
		return FunctionType;
	}
	public void setFunctionType(Integer functionType) {
		FunctionType = functionType;
	}
	
}
